    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        // Bouton Recherche
        if (jTextField5.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Entrez la recherche");
        } else {
            if (jComboBox1.getSelectedItem().equals("ID")) {
                rs = db.querySelectAll("client", "IdClient LIKE '%" + jTextField5.getText() + "%' ");
                jTable1.setModel(new ResultSetTableModel(rs));
            } else if (jComboBox1.getSelectedItem().equals("Prénom")) {
                rs = db.querySelectAll("client", "Prenom LIKE '%" + jTextField5.getText() + "%' ");
                jTable1.setModel(new ResultSetTableModel(rs));
            } else if (jComboBox1.getSelectedItem().equals("Nom")) {
                rs = db.querySelectAll("client", "Nom LIKE '%" + jTextField5.getText() + "%' ");
                jTable1.setModel(new ResultSetTableModel(rs));
            } else if (jComboBox1.getSelectedItem().equals("Code Postal")) {
                rs = db.querySelectAll("client", "CP LIKE '%" + jTextField5.getText() + "%' ");
                jTable1.setModel(new ResultSetTableModel(rs));
            } else if (jComboBox1.getSelectedItem().equals("Email")) {
                rs = db.querySelectAll("client", "Mail LIKE '%" + jTextField5.getText() + "%' ");
                jTable1.setModel(new ResultSetTableModel(rs));
            } else if (jComboBox1.getSelectedItem().equals("Téléphone")) {
                rs = db.querySelectAll("client", "Tel LIKE '%" + jTextField5.getText() + "%' ");
                jTable1.setModel(new ResultSetTableModel(rs));
            } else if (jComboBox1.getSelectedItem().equals("Id Entreprise")) {
                rs = db.querySelectAll("client", "id_entreprise LIKE '%" + jTextField5.getText() + "%' ");
                jTable1.setModel(new ResultSetTableModel(rs));
            }
        }
    }//GEN-LAST:event_jButton10ActionPerformed

